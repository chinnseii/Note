    USER_MOBILE VARCHAR(11) NOT NULL PRIMARY KEY,
    SIGNATURE VARCHAR(255),
    FOLLOW VARCHAR(),
    FAN VARCHAR(),
    STAR VARCHAR(),
    USER_LEVEL CHAR(2),
    EXP CHAR (5),
